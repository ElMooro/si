def get_unusual():









    tickers=['SPY','QQQ','AAPL','TSLA','NVDA','AMD','AMZN','META','MSFT','GOOGL','NFLX','COIN','PLTR','SOFI']









    end=datetime.utcnow().strftime('%Y-%m-%d'); start=(datetime.utcnow()-timedelta(days=30)).strftime('%Y-%m-%d')









    urls = {t: f"https://api.polygon.io/v2/aggs/ticker/{t}/range/1/day/{start}/{end}?adjusted=true&sort=desc&limit=21&apiKey={POLYGON_KEY}" for t in tickers}









    data = fetch_all(urls, 8)









