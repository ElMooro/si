def generate_signals(vix, skew, pc, gex, sentiment, ff):









    signals = []









    v = vix.get('vix', {}).get('current')









    if v:









        if v > 30: signals.append({'type':'CONTRARIAN_BUY','strength':'STRONG','source':'VIX','message':f'VIX at {v:.1f} - extreme fear','confidence':min(90,50+int((v-30)*3))})









        elif v < 13: signals.append({'type':'CONTRARIAN_SELL','strength':'MODERATE','source':'VIX','message':f'VIX at {v:.1f} - complacency','confidence':min(85,50+int((13-v)*5))})









    if vix.get('term_structure',{}).get('state')=='BACKWARDATION':









        signals.append({'type':'WARNING','strength':'STRONG','source':'VIX_TERM','message':'VIX backwardation - precedes 70%+ of crashes','confidence':75})









    if skew.get('current',0) > 145:









        signals.append({'type':'TAIL_RISK','strength':'STRONG','source':'SKEW','message':f'SKEW {skew["current"]:.0f} - heavy crash protection','confidence':70})









    tpc = pc.get('total_put_call_ratio', 0)









    if tpc > 1.3: signals.append({'type':'CONTRARIAN_BUY','strength':'STRONG','source':'PUT_CALL','message':f'P/C {tpc:.2f} - extreme put buying','confidence':80})









    elif 0 < tpc < 0.5: signals.append({'type':'CONTRARIAN_SELL','strength':'MODERATE','source':'PUT_CALL','message':f'P/C {tpc:.2f} - extreme call buying','confidence':70})









    if gex.get('regime')=='NEGATIVE_GAMMA':









        signals.append({'type':'VOL_WARNING','strength':'MODERATE','source':'GAMMA','message':'Negative GEX - dealers amplifying moves','confidence':65})









    ra=ff.get('risk_appetite',{})









    if ra.get('regime')=='RISK_OFF':









        signals.append({'type':'RISK_OFF','strength':'MODERATE','source':'FUND_FLOWS','message':f'${abs(ra.get("net_flow",0))/1e6:.0f}M rotating to safe havens','confidence':60})









    comp=sentiment.get('composite',{}).get('score',50)









    if comp<20: signals.append({'type':'CONTRARIAN_BUY','strength':'STRONG','source':'SENTIMENT','message':f'Sentiment {comp:.0f} - extreme fear','confidence':85})









    elif comp>80: signals.append({'type':'CONTRARIAN_SELL','strength':'STRONG','source':'SENTIMENT','message':f'Sentiment {comp:.0f} - extreme greed','confidence':80})









    signals.sort(key=lambda x: x.get('confidence',0), reverse=True)









    return signals



















