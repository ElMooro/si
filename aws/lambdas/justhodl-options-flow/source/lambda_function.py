def get_sentiment(fred):









    result = {}









    vix_data = fred.get('VIXCLS', [])









    if vix_data:









        c=vix_data[0]['value']; h=[v['value'] for v in vix_data]; pct=calc_percentile(c,h)









        vs=max(0,min(100,100-pct)) if pct else 50









        result['vix_sentiment']={'score':round(vs,1),'label':'Fear' if vs<30 else('Greed' if vs>70 else 'Neutral'),'vix':c}









    ted=fred.get('TEDRATE',[]); ted_v=ted[0]['value'] if ted else None









    if ted_v is not None:









        cs=15 if ted_v>1 else(35 if ted_v>0.5 else(55 if ted_v>0.25 else 80))









        result['credit_sentiment']={'score':cs,'ted_spread':ted_v,'label':'Fear' if cs<30 else('Greed' if cs>70 else 'Neutral')}









    t10y2y=fred.get('T10Y2Y',[]); sp=t10y2y[0]['value'] if t10y2y else None









    if sp is not None:









        cvs=20 if sp<0 else(40 if sp<0.5 else(60 if sp<1 else 80))









        result['curve_sentiment']={'score':cvs,'spread':sp,'label':'INVERTED' if sp<0 else 'Normal'}









    hy=fred.get('BAMLH0A0HYM2',[]); hv=hy[0]['value'] if hy else None









    if hv is not None:









        hs=10 if hv>6 else(30 if hv>4.5 else(55 if hv>3.5 else 80))









        result['hy_sentiment']={'score':hs,'spread':hv,'label':'Stress' if hs<30 else('Calm' if hs>70 else 'Watchful')}









    # Breadth from SPY









    spy=safe_fetch(f"https://api.polygon.io/v2/aggs/ticker/SPY/prev?adjusted=true&apiKey={POLYGON_KEY}",6)









    bs=50









    if spy.get('results'):









        bar=spy['results'][0]; chg=((bar['c']-bar['o'])/bar['o'])*100; bs=max(0,min(100,50+chg*15))









    result['breadth_sentiment']={'score':round(bs,1),'label':'Weak' if bs<35 else('Strong' if bs>65 else 'Neutral')}









    # RSI from SPY bars









    bars_url=f"https://api.polygon.io/v2/aggs/ticker/SPY/range/1/day/{(datetime.utcnow()-timedelta(days=30)).strftime('%Y-%m-%d')}/{datetime.utcnow().strftime('%Y-%m-%d')}?adjusted=true&sort=desc&limit=20&apiKey={POLYGON_KEY}"









    bars_data=safe_fetch(bars_url,6)









    spy_bars=bars_data.get('results',[])









    if spy_bars and len(spy_bars)>=14:









        gains=[]; losses=[]









        for i in range(1,min(15,len(spy_bars))):









            ch=spy_bars[i-1].get('c',0)-spy_bars[i].get('c',0)









            gains.append(max(0,ch)); losses.append(max(0,-ch))









        ag=sum(gains)/len(gains) if gains else 0; al=sum(losses)/len(losses) if losses else 1









        rs=ag/al if al>0 else 100; rsi=100-(100/(1+rs))









        result['momentum_sentiment']={'score':round(rsi,1),'rsi':round(rsi,1),'label':'Oversold' if rsi<30 else('Overbought' if rsi>70 else 'Neutral')}









    # News









