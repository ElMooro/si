def fetch_all(urls, timeout=8):









    results = {}









    def _fetch(item):









        key, url = item









        return key, safe_fetch(url, timeout)









    with concurrent.futures.ThreadPoolExecutor(max_workers=12) as ex:









        for key, data in ex.map(_fetch, urls.items()):









            results[key] = data









    return results



















