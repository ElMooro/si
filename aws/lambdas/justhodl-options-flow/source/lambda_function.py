def calc_percentile(cur, hist):









    if not hist or cur is None: return None









    return round(sum(1 for v in hist if v < cur) / len(hist) * 100, 1)



















def calc_zscore(cur, hist):









    if not hist or len(hist) < 2: return None









    m = sum(hist)/len(hist); v = sum((x-m)**2 for x in hist)/len(hist); s = math.sqrt(v) if v > 0 else 1









    return round((cur-m)/s, 2)



















def calc_sma(vals, p):









    return round(sum(vals[:p])/p, 4) if len(vals) >= p else None



















